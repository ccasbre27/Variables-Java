
public class Main 
{

	public static void main(String[] args) 
	{
		// número entero
		int edad = 9;
		
		// número decimal
		double altura = 175.5;
		
		// cadena de caracteres
		String cadenaCaracteres = "Soy una cadena de caracteres";
		
		// boolean
		boolean administrador = true;
		
		System.out.println("Variable entera: " + edad);
		System.out.println("Variable decimal: " + altura);
		System.out.println("Variable cadena de caracteres: " + cadenaCaracteres);
		System.out.println("Variable boolean: " + administrador);

	}

}
